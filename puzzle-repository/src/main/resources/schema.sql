-------- COLOR -----

DROP TABLE COLOR IF EXISTS;

CREATE TABLE COLOR (
WORDING VARCHAR(30),
PRIMARY KEY(WORDING)
);

--------- BALL PART -----
DROP TABLE BALL_PART IF EXISTS;

CREATE TABLE BALL_PART (
WORDING VARCHAR(30),
PRIMARY KEY(WORDING)
);

----- TASK ------

DROP TABLE TASK IF EXISTS;

CREATE TABLE TASK (
ID INT GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) NOT NULL,
EXT_TASK_ID VARCHAR(100) NOT NULL
START_TIME TIMESTAMP NOT NULL
);

----- TASK_PUZZLE_MAP ------

DROP TABLE TASK_PUZZLE_MAP IF EXISTS;

CREATE TABLE TASK_PUZZLE_MAP (
TASK_ID INT NOT NULL,
SHIFT_X INT NOT NULL,
SHIFT_Y INT NOT NULL
);

----- TASK_DETAILS ----------
DROP TABLE TASK_DETAILS IF EXISTS;

CREATE TABLE TASK_DETAILS (
TASK_ID INT NOT NULL,
COLOR_LEFT_SIDE VARCHAR(30) NOT NULL,
PART_LEFT_SIDE VARCHAR(30) NOT NULL,
COLOR_UPPER_SIDE VARCHAR(30) NOT NULL,
PART_UPPER_SIDE VARCHAR(30) NOT NULL,
COLOR_RIGHT_SIDE VARCHAR(30) NOT NULL,
PART_RIGHT_SIDE VARCHAR(30) NOT NULL,
COLOR_LOWER_SIDE VARCHAR(30) NOT NULL,
PART_LOWER_SIDE VARCHAR(30) NOT NULL,
);


