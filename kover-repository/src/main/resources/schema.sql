-------- COLOR -----

DROP TABLE COLOR IF EXISTS;

CREATE TABLE COLOR (
WORDING VARCHAR(30),
PRIMARY KEY(WORDING)
);

--------- BALL PART -----
DROP TABLE BALL_PART IF EXISTS;

CREATE TABLE BALL_PART (
WORDING VARCHAR(30),
PRIMARY KEY(WORDING)
);


------ BALL SIDE -------
DROP TABLE BALL_SIDE if exists;

create table BALL_SIDE (
id INT GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) NOT NULL,
COLOR VARCHAR(30),
BALL_PART VARCHAR(30),
PRIMARY KEY(ID)
);

------ DETAIL ------

DROP TABLE DETAIL IF EXISTS;

CREATE TABLE DETAIL (
ID INT GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) NOT NULL,
LEFT_SIDE INT NOT NULL,
UPPER_SIDE INT NOT NULL,
RIGHT_SIDE INT NOT NULL,
LOWER_SIDE INT NOT NULL
);

